import { ChangeEvent, KeyboardEvent, useEffect, useState } from 'react';
import { styled, alpha } from '@mui/material/styles';
import InputBase from '@mui/material/InputBase';
import SearchIcon from '@mui/icons-material/Search';
import { useRouter } from 'next/router';

interface SearchInputProps {
  value: (value: string) => void
}

const Search = styled('div')(({ theme }) => ({
  position: 'relative',
  borderRadius: theme.shape.borderRadius,
  backgroundColor: alpha(theme.palette.common.white, 0.15),
  '&:hover': {
    backgroundColor: alpha(theme.palette.common.white, 0.25),
  },
  marginLeft: 0,
  width: '100%',
  [theme.breakpoints.up('sm')]: {
    marginLeft: theme.spacing(1),
    width: 'auto',
  },
}));

const SearchIconWrapper = styled('div')(({ theme }) => ({
  padding: theme.spacing(0, 2),
  height: '100%',
  position: 'absolute',
  pointerEvents: 'none',
  display: 'flex',
  alignItems: 'center',
  justifyContent: 'center',
}));

const StyledInputBase = styled(InputBase)(({ theme }) => ({
  color: 'inherit',
  width: '100%',
  '& .MuiInputBase-input': {
    padding: theme.spacing(1, 1, 1, 0),
    // vertical padding + font size from searchIcon
    paddingLeft: `calc(1em + ${theme.spacing(4)})`,
    transition: theme.transitions.create('width'),
    [theme.breakpoints.up('sm')]: {
      width: '12ch',
      '&:focus': {
        width: '20ch',
      },
    },
  },
}));

export default function SearchInput({ value }: SearchInputProps) {
  const router = useRouter()
  const [valueInput, setValueInput] = useState(router.query.title as string ?? '')

  const setSearchValue = (event: KeyboardEvent<HTMLDivElement>) => {
    if (event.key === 'Enter') {
      value(valueInput)
      router.push({
        query: {
          title: valueInput
        }
      });
    }
  }

  const onChangeValue = (event: ChangeEvent<HTMLInputElement>) => {
    setValueInput(event.target.value)
  }

  return (
    <Search>
      <SearchIconWrapper>
        <SearchIcon />
      </SearchIconWrapper>
      <StyledInputBase
        placeholder="Searchâ€¦"
        inputProps={{ 'aria-label': 'search' }}
        onChange={onChangeValue}
        onKeyDownCapture={setSearchValue}
        defaultValue={valueInput}
      />
    </Search>
  );
}
